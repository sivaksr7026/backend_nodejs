const mongoose = require('mongoose')

const teacherSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  phone: {
    type: Number,
    required: true
  }
})

module.exports = mongoose.model('Teacher', teacherSchema)
